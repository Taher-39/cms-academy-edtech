/** Get-or-create the singleton settings doc, seeding default content on first call. */
export async function getSettings() {
  await connectToDB();

  let settings = await SettingsModel.findOne({});
  if (!settings) {
    settings = await SettingsModel.create({
      banners: [],
      faqs: seedFaqs,
      termsContent: seedTermsContent,
    });
  }

  return { settings };
}

export async function updateSettings(data: any, adminId: string) {
  await connectToDB();

  let settings = await SettingsModel.findOne({});
  if (!settings) {
    settings = new SettingsModel({});
  }

  if (data.banners !== undefined) settings.banners = data.banners;
  if (data.faqs !== undefined) settings.faqs = data.faqs;
  if (data.termsContent !== undefined) settings.termsContent = data.termsContent;
  settings.updatedBy = adminId as any;

  await settings.save();

  return { message: "সেটিংস আপডেট করা হয়েছে", settings };
}
